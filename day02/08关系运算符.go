package main

import "fmt"

func main0801() {
	a := 10
	b := 20
	// == 相等于运算符
	fmt.Println("相等于：", a == b)
	// == 不等于运算符
	fmt.Println("不等于：", a != b)
	// == 大于运算符
	fmt.Println("大于：", a > b)
	// == 小于运算符
	fmt.Println("小于：", a < b)
	// == 大于等于运算符
	fmt.Println("大于等于：", a >= b)
	// == 小于等于运算符
	fmt.Println("小于等于：", a <= b)
}
